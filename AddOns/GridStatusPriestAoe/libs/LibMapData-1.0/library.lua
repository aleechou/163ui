		if v.floors > 0 and floors > 0 then
			for f = 1, floors do
				SetDungeonMapLevel(f)
				local _, l, t, r, b = GetCurrentMapDungeonLevel()
				if l and l ~= 0 and t and t~= 0 and r and r ~= 0 and b and b ~= 0 then
					if v[f] == nil then
						v[f] = {}
					end
					local width, height = 0,0
					width = math.abs((-l) - (-r))
					height = math.abs(t - b)
					if v[f][3] ~= -l  and v[f][4] ~= t and v[f][5] ~= -r and v[f][6] ~= b then
						v[f][1] = width
						v[f][2] = height
						v[f][3] = -l
						v[f][4] = t
						v[f][5] = -r
						v[f][6] = b
					end
				end
			end
		end
	end
end

--- API to encode a location in game
-- @param x
-- @param y
-- @param mapLevel
-- @return encoded location number
function lib:EncodeLoc(x,y,level)
	local level = level or 0
	if x > 0.9999 then
		x = 0.9999
	end
	if y > 0.9999 then
		y = 0.9999
	end
	return floor( x * 10000 + 0.5 ) * 1000000 + floor( y * 10000  + 0.5 ) * 100 + level
end

--- API to decode a location num
-- @param location id
-- @return x,y,level
function lib:DecodeLoc(id)
	return floor(id/1000000)/10000, floor(id % 1000000 / 100)/10000, id % 100
end

